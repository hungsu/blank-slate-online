export function getRandomPrompt(usedIndices: Set<number>): { prompt: string; index: number } | null {
  const available = PROMPTS.map((_, i) => i).filter((i) => !usedIndices.has(i));
  if (available.length === 0) return null;
  const idx = available[Math.floor(Math.random() * available.length)];
  return { prompt: PROMPTS[idx], index: idx };
}
